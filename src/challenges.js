// Desafio 6
function highestCount(arr) {
  let hi = arr[0];
  let count = 0;
  for (let num of arr) {
    if (num > hi) {
      hi = num;
      count = 0;
    }
    if (num === hi) {
      count += 1;
    }
  }
  return count;
}

